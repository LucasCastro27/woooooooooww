    public GaragemCarros() throws FileNotFoundException {
        
        //inicia as variaveis para a leitura do Arquivo .Txt contendo as informações
        Frota = new ArrayList<>();
        File arquivo = new File("carros.txt");
        Scanner leitura = new Scanner(arquivo);

        //Le os Arquivo txt enquanto inicia as variaveis carro
        String dados = "";
        numeroCarro = 0;
        int contador = 0;
        while (leitura.hasNextLine()) {
            dados = dados + leitura.nextLine() + '\n';
            contador++;
            if (contador == 8) {
                contador = 0;
                carro veiculo = new carro(dados);
                Frota.add(veiculo);
                dados = "";
                numeroCarro++;
            }
        }
        
        //funão GUI
        initComponents();
        
        // Pega os nomes do carros Do vetor de objetos Carros e os escreve na lista
        Lista= new DefaultListModel<>();
        for (int i = 0; i < numeroCarro; i++) {
            Lista.addElement(Frota.get(i).modelo);
        }
        jList1.setModel(Lista);
        
        
        // Inicia a Parte das informações com os dados do carro no primeiro indice
        String aux = "";
        marca.setText(Frota.get(0).marca);
        cor.setText(Frota.get(0).cor);
        placa.setText(Frota.get(0).placa);
        aux = Frota.get(0).kmAndado + "";
        km.setText(aux);
        aux = "";
        aux = Frota.get(0).preco + "";
        preco.setText(aux);
        if (Frota.get(0).disponivel == 0) {
            disponivel.setText("nao");
        } else {
            disponivel.setText("sim");
        }
    }

