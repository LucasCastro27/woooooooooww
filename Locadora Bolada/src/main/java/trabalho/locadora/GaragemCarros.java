    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        //Botão de editar tranfere as informações feitas nas caixas para o vetor de carros
        
        if(disponivel.getText().charAt(0)=='s')
            Frota.get(selecionado).disponivel=1;
        else 
            Frota.get(selecionado).disponivel=0;
        
        Frota.get(selecionado).cor=cor.getText();
        Frota.get(selecionado).placa=placa.getText();
        Frota.get(selecionado).marca=marca.getText();
        
        String aux ="";
        aux=km.getText();
        Frota.get(selecionado).kmAndado=Integer.parseInt(aux);
        
        aux=preco.getText();
        Frota.get(selecionado).preco=Float.parseFloat(aux);
                
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        //Botão de Salvar escreve a nova alteração do vetor no arquivo .txt
        String dados="";
        for(int i=0;i<numeroCarro;i++)
        {
            dados=dados+"placa="+Frota.get(i).placa+'\n';
            dados=dados+"marca="+Frota.get(i).marca+'\n';
            dados=dados+"preco="+Frota.get(i).preco+'\n';
            dados=dados+"KmAndado="+Frota.get(i).kmAndado+'\n';
            dados=dados+"cor="+Frota.get(i).cor+'\n';
            dados=dados+"dataRetorno="+Frota.get(i).dataRetorno+'\n';
            dados=dados+"disponivel="+Frota.get(i).disponivel+'\n';
            dados=dados+"modelo="+Frota.get(i).modelo+'\n';
        }
        try {
            BufferedWriter writer = new BufferedWriter(new FileWriter("carros.txt"));
            
            
            writer.write(dados);
            writer.close();
        } catch (IOException ex) {
            Logger.getLogger(GaragemCarros.class.getName()).log(Level.SEVERE, null, ex);
        }
        

    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVenderActionPerformed
        //botão de venda permite a Remoção dos carros da lista
        
        Frota.remove(selecionado);
        numeroCarro--;
        Lista= new DefaultListModel<>();
        for (int i = 0; i < numeroCarro; i++) {
            Lista.addElement(Frota.get(i).modelo);
        }
        jList1.setModel(Lista);
    }//GEN-LAST:event_jButtonVenderActionPerformed


